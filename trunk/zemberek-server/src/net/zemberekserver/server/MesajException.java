package net.zemberekserver.server;

/**
 * @author MDA
 *
 */
public class MesajException extends Exception {

    public MesajException() {
        super();
    }

    public MesajException(String arg0) {
        super(arg0);
    }

    public MesajException(Throwable arg0) {
        super(arg0);
    }

    public MesajException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
}
